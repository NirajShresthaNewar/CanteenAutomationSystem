import 'package:http/http.dart' as http;
import 'package:network_info_plus/network_info_plus.dart';
import 'dart:io' show Platform;
import 'package:flutter/foundation.dart' show kIsWeb;

class AppConfig {
  static String _baseUrl = '';

  static String get baseUrl => _baseUrl;

  static Future<String?> getLocalIpAddress() async {
    // Skip IP detection on web platform
    if (kIsWeb) {
      print('Running on web platform, skipping IP detection');
      return null;
    }
    
    try {
      final info = NetworkInfo();
      return await info.getWifiIP(); // returns like '192.168.1.70'
    } catch (e) {
      print('Error getting local IP: $e');
      return null;
    }
  }

  static Future<void> detectServerIP() async {
    try {
      print('Attempting to detect server IP...');

      List<String> serverAddresses = [
        '192.168.1.7',
        '192.168.56.10',
        'localhost',
        '127.0.0.1',
      ];

      // Detect device's local IP and add variants (skip on web)
      if (!kIsWeb) {
        final localIp = await getLocalIpAddress();
        if (localIp != null && !serverAddresses.contains(localIp)) {
          serverAddresses.insert(0, localIp);
          serverAddresses.insert(1, '$localIp:80');
        }
      }

      print('Testing server addresses: $serverAddresses');

      for (String address in serverAddresses) {
        try {
          final testUrl =
              'http://$address/CanteenAutomationSystem/api/auth/login.php';
          print('Trying: $testUrl');

          final response = await http.get(Uri.parse(testUrl)).timeout(
                const Duration(seconds: 10),
              );

          print('Response from $address: ${response.statusCode}');
          if ([200, 400, 403, 404, 405].contains(response.statusCode)) {
            _baseUrl = 'http://$address/CanteenAutomationSystem';
            print('‚úÖ Server found at: $_baseUrl');
            return;
          }
        } catch (e) {
          print('‚ùå Failed to connect to $address: $e');
        }
      }

      _baseUrl = 'http://${serverAddresses[0]}/CanteenAutomationSystem';
      print('‚ö†Ô∏è No server found, using fallback: $_baseUrl');
    } catch (e) {
      print('üí• Fatal error: $e');
      _baseUrl = 'http://localhost/CanteenAutomationSystem';
    }
  }

  static String getBaseUrl() {
    print('Current base URL: $_baseUrl');
    return _baseUrl;
  }
}






























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































